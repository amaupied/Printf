int		ft_factorial(int nb)
{
	if (nb < 1 || nb > 12)
	{
		if (nb == 0)
			nb = 1;
		else
			nb = 0;
	}
	if (nb > 2)
		nb = nb * ft_factorial(nb - 1);
	return (nb);
}
